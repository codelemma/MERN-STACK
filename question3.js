// 3.WAP to print area and perimeter of rectangle , taking input from the user , using prompt
var length = prompt("Please enter the length");
var breadth = prompt("Enter the breadth");
var areaOfRectangle = length * breadth;
console.log("Area of the Rectangle is : "+ areaOfRectangle);
var pm = (parseInt(length) + parseInt(breadth)) * 2;
console.log("Perimeter is : "+pm);


